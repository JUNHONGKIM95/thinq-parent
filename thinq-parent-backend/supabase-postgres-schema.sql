  created_at timestamptz not null default now()
);

comment on table routine_master is '임신 시기별 가전 루틴 마스터. 초기/중기/후기 3개.';

create table if not exists routine_action (
  routine_action_id bigint generated by default as identity primary key,
  routine_id bigint not null references routine_master(routine_id) on delete cascade on update cascade,
  appliance_master_id bigint not null references appliance_master(appliance_master_id) on delete restrict on update cascade,
  sequence_no integer not null check (sequence_no > 0),
  action_title varchar(100) not null,
  action_description text,
  target_power_state varchar(20),
  target_mode varchar(50),
  target_alert_sound varchar(20),
  created_at timestamptz not null default now(),
  unique (routine_id, sequence_no)
);

comment on table routine_action is '루틴별 각 가전의 동작 정의. 루틴 선택 시 가전이 어떻게 동작하는지 표시용.';

