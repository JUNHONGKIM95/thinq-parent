create table if not exists routine_master (
  routine_id bigint generated by default as identity primary key,
  routine_code varchar(30) not null unique,
  pregnancy_stage varchar(20) not null unique
    check (pregnancy_stage in ('EARLY', 'MIDDLE', 'LATE')),
